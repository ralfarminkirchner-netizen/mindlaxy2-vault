/**
 * Filters and deduplicates string values.
 * @param {string[]} values - Array of strings to filter and deduplicate
 * @returns {string[]} Unique, truthy string values
 */
function uniqueStrings(values) {
  return Array.from(new Set(values.filter(Boolean)));
}

/**
 * Determines risk level from risk value components.
 * @param {{overidentification: number, bypass: number, trauma: number}} risks - Risk component values
 * @returns {'low' | 'medium' | 'high'} The calculated risk level
 */
function riskLevelFromValues(risks) {
  const average = (risks.overidentification + risks.bypass + risks.trauma) / 3;
  if (average >= 7) return 'high';
  if (average >= 4) return 'medium';
  return 'low';
}

/**
 * Factory function to create a complete SharedPracticeRecord from seed data.
 * @param {{id: string, slug: string, source: string, sourceId: string, title: string, summary: string, tradition: string, states: string[], tai: {tolerance: number, authenticity: number, integrity: number}, risks: {overidentification: number, bypass: number, trauma: number}, primaryEffect: string, secondaryEffects: string[], practicalSteps: string[], typicalDurationMinutes: number, frequencyRecommendation: string, preparation: string, integrationTips: string, stagesProgression: string, typicalChallenges: string, contraindications: string, [key: string]: any}} seed - Practice data without derived fields
 * @returns {SharedPracticeRecord} Complete practice record with calculated fields
 */
function createPractice(seed) {
  const average = Number(
    ((seed.tai.tolerance + seed.tai.authenticity + seed.tai.integrity) / 3).toFixed(1)
  );

  return {
    ...seed,
    tai: {
      ...seed.tai,
      average,
    },
    risks: {
      ...seed.risks,
      level: riskLevelFromValues(seed.risks),
    },
    relatedNodeIds: uniqueStrings(IBV_ATLAS_NODE_LINKS[seed.sourceId] ?? []),
  };
}

